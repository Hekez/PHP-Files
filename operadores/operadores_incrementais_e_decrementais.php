<?php

	$a = 10;
	$b = 10;

	//Incremento
	echo ++$a . "<br/>"; //Soma 10 + 1
	echo $a++ . "<br/>"; //Mostra 10 e soma +1
	echo $a . "<br/>"; //Resultado da incrementação anterior

	//Decremento
	echo --$b . "<br/>"; //Subtrai 10 - 1
	echo $b-- . "<br/>"; //Mostra 10 e subtrai -1
	echo $b; //Resultado da incrementação anterior
	
?>