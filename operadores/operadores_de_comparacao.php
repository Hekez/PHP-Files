<?php

	$a = 1;
	$b = 2.0;

	//Maior
	var_dump($a > $b);
	echo "<br/><br/>";

	//Menor
	var_dump($a < $b);
	echo "<br/><br/>";

	//Atribui valor
	var_dump($a = $b);
	echo "<br/><br/>";

	//Compara valor
	var_dump($a == $b);
	echo "<br/><br/>";

	//Compara o tipo da variável
	var_dump($a === $b);
	echo "<br/><br/>";

	//Verifica a diferença
	var_dump($a != $b);
	echo "<br/><br/>";

	//Verifica a diferença do tipo de dado da variável
	var_dump($a !== $b);
	echo "<br/><br/>";

	//Maior ou igual
	var_dump($a >= $b);
	echo "<br/><br/>";

	//Menor ou igual
	var_dump($a <= $b);
	echo "<br/><br/>";

?>